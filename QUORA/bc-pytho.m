For the area to equal the perimeter, we have: [math]a+b+c=\frac{a b}{2}[/math]

For the triangle to be a right triangle we have: [math]a^2+b^2=c^2[/math]

The only non-trivial solutions to these simultaneous equations are:

[math]\left\{c\to b+\frac{8}{b-4},a\to \frac{8}{b-4}+4\right\}[/math]

For [math]a[/math] to be an integer, [math]\frac{8}{b-4}[/math] must be as well. The factors of 8 are [math]\{1,2,4,8\}[/math], so [math]b-4\in \{1,2,4,8\}[/math], so [math]b\in \{5,6,8,12\}[/math]

Taking these one at a time:

  - [math]b=5\Rightarrow \{c\to 13,a\to 12\}[/math], so the triangle is [math]\{5,12,13\}[/math]

  - [math]b=6\Rightarrow \{c\to 10,a\to 8\}[/math], so the triangle is [math]\{6,8,10\}[/math]

  - [math]b=8\Rightarrow \{c\to 10,a\to 6\}[/math], so the triangle is just a repeat of the above.

  - [math]b=12\Rightarrow \{c\to 13,a\to 5\}[/math], which is a repeat of the first case.

Note that we expected duplicates since [math]a[/math] and [math]b[/math] are symmetrical in this problem.

So, the two answers are [math]\{5,12,13\}[/math] and [math]\{6,8,10\}[/math]

Note that [math]\{3,4,5\}[/math] is the same triangle (by congruency) as [math]{6,8,10}[/math], but [math]{3,4,5}[/math] is not a solution, because doubling the lengths quadruples the area.
